<p>Você pode fazer uma consulta via Vídeo Chamada com audio e video ao vivo dentro do site, consulte os tarólogos disponíveis. Tanto o tarólogo como o cliente não tem obrigação de mostrar o rosto. Se a consulta for do tipo videochamada é possível deixar a camera desligada, ou não mostrar o rosto.</p>

